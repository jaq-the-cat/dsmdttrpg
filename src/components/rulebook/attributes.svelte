<section id="attributes">
  <h1>Attributes</h1>
  <p>
    Attributes go from 0 to 10, and give +1 to the relevant Dice roll per level,
    starting at -4. (10 gives +6)
  </p>

  <h1>Stats</h1>
  <ul>
    <li>Vitality</li>
    <li>Strength</li>
    <li>Dexterity</li>
    <li>Charisma</li>
    <li>Perception</li>
    <li>Intelligence</li>
  </ul>

  <h1>Skills</h1>
  <p>
    Skills have the same roll bonus as the parent Stat (from -4 to +6), but can
    be further modified by having Proficiency, Expertise or something else
    affecting that check, such as Advantage. Your Passive Perception is your
    Perception Stat plus your Perception modifier, up to 14. Your Passive Dodge
    is your Agility Stat plus your Agility Modifier, up to 12.
  </p>
  <ul>
    <li>Athletics (Vitality)</li>
    <li>Acrobatics (Agility)</li>
    <li>Stealth (Agility)</li>
    <li>
      <span>Flying (Agility)</span>
      <small>Exclusive to Solver, Disassembly Drones and Avian Drones</small>
    </li>
    <li>Melee (Strength or Dexterity)</li>
    <li>Firearms (Dexterity)</li>
    <li>Persuasion (Charisma)</li>
    <li>Intimidation (Charisma)</li>
    <li>Investigation (Perception)</li>
    <li>Knowledge (Intelligence)</li>
    <li>Technology (Intelligence)</li>
    <li>Explosives (Intelligence + Dexterity)</li>
    <li>Medicine (Intelligence + Dexterity)</li>
    <li>Mechanics (Intelligence + Dexterity)</li>
    <li>
      <span>Willpower (highest of Strength, Charisma or Intelligence)</span>
      <small>Exclusive to Solver-enabled Drones</small>
    </li>
  </ul>
</section>

