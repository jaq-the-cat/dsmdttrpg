  <summary>Combat</summary>
  <p>
    When Combat begins, every member rolls an Initiative roll, which determines
    the order at which turns are taken (higher being earlier).
  </p>
  <p>
    During your turn, you have 4 Actions, which can be an attack, movement,
    dash, weapon swap, attempt to hide, etc. You can attempt any action in any
    order. You get an extra Reaction if you don't attack during your turn, up to
    5.
  </p>
  <p>
    Remaining actions can be used in between turns as Reactions. If you Attack 3
    times and end your turn, you have 1 Reaction remaining. If you dash, move,
    swap weapons, and attack, you don't have any reactions.
  </p>
  <p>
    A Reaction can be dodging, blocking, or an Attack of Opportunity, which you
    can do when an enemy tries to run away from you from melee distance.
  </p>
  <p>
    Enemies only have 2 actions, and always end their turn with 2 reactions.
  </p>
  <p>
    If the enemy is an unarmed Human/Worker Drone, they do not get any actions
    besides moving/dashing. If in Melee range and you have any weapon equipped,
    roll a 1d20 Brutality roll and describe how you kill them. A Nat 1 fails to
    kill.
  </p>
  <h3>Available Actions</h3>
  <ul>
    <li>Attack</li>
    <ul>
      <li>
        Melee attacks only have to do a hit check if the enemy is Dodging,
        Blocking or running away (see Attack of Opportunity)
      </li>
      <li>
        Ranged Attacks from range must always use a Firearms (guns) or Athletics
        (throwing) hit check, against either their Passive Dodge or Agility
        check if they attempt to Dodge. From point blank range, only require a
        hit check if the enemy is aware and dodging.
      </li>
      <li>Assassination</li>
      <ul>
        <li>
          An Assassination is a special attack that, when performed from
          Stealth, will not get you out of Stealth.
        </li>
        <li>
          Examples of valid assassination scenarios are coming from a Hiding
          spot, attacking someone out of combat, or attacking an enemy that is
          unaware of you.
        </li>
        <li>
          Any low-caliber Silenced weapon or knife can be used to perform an
          Assassination.
        </li>
      </ul>
    </ul>
    <li>
      Targeted Attack<br />
      Aim for a specific limb with a penalty to accuracy. On hit, remove it. If it's
      the head, instantly kill the opponent.
    </li>
    <ul>
      <li>Head: -4</li>
      <li>Arms: -3</li>
      <li>Legs: -3</li>
    </ul>
    <li>Move</li>
    <ul>
      <li>Move at Walking speed.</li>
      <li>If previous action was Sprint, move at Running speed.</li>
      <li>
        If Wings are active, and there is enough space, move at Flying Speed.
      </li>
    </ul>
    <li>
      Activate/Deactivate Wings
      <br /><small
        >Avian Drones do not use an Action to activate their wings.</small
      >
    </li>
    <li>
      Equip/Swap/Unequip Item
      <ul>
        <li>Disassembly Drones gain 1 Heat when switching Arm Weapons.</li>
      </ul>
    </li>
    <li>Hide</li>
    <ul>
      <li>
        Stop where you are and attempt to hide with a Stealth roll. You can't
        move while Hiding and enemies with a higher Passive Perception than your
        Stealth roll will be able to spot you. The DM can add modifiers to your
        roll depending on where you are trying to hide.
      </li>
    </ul>
    <li>Crouch</li>
    <ul>
      <li>
        Crouch and move quietly. Enemies cannot see you if they do not have Line
        of Sight and you will not make noise while walking. While Crouching,
        your speed is reduced to 4m. If you Dash while Crouching, you can move
        at 6m but make Noise up to 1m away.
      </li>
    </ul>
    <li>Change Stance</li>
    <small
      >While in a Stance, you can't do any other actions besides what it
      explicitly allows you to do</small
    >
    <ul>
      <li>Ambushing</li>
      <ul>
        <li>Allows Attacking.</li>
        <li>Gives +3 Hit chance where you're looking.</li>
        <li>Reduces movement to 3 meters and prevents Dashing.</li>
        <li>Leaving this Stance is free.</li>
      </ul>
      <li>Dodging</li>
      <ul>
        <li>Allows Dodging.</li>
        <li>
          Sets your Passive Dodge to an Acrobatics roll +2 and ends your turn.
        </li>
        <li>
          Any Dodge reactions done while in Dodging stance also gain a +2.
        </li>
        <li>
          If you see a Melee attack coming, they must do a Hit check against
          your Passive Dodge even if you don't use a reaction.
        </li>
        <li>Leaving this Stance is free.</li>
      </ul>
      <li>Bracing (must have a blocking item)</li>
      <ul>
        <li>Allows Blocking.</li>
        <li>Gives +3 to reaction Blocking.</li>
        <li>Leaving this Stance costs 1 Action.</li>
      </ul>
    </ul>
    <li>Grapple</li>
    <ul>
      <li>
        Run a Melee check against your target, and they can use an Athletics,
        Acrobatics or Melee check to counter it.
      </li>
      <li>If you win, you grapple them to the ground, stunning them.</li>
      <li>If they win, you immediately end your turn.</li>
    </ul>
    <li>Throw</li>
    <ul>
      <li>
        Run a Strength Melee check against your target, and they can use an
        Athletics or Strength check to counter it.
      </li>
      <li>
        If you win, you can throw them up to 1d4 + Strength meters away. You
        deal fall damage multiplied by 2 based on that roll.
      </li>
      <li>
        If they have a Jump Height of 4 and you throw them 10 meters, you deal
        (10-4)*2=12 damage
      </li>
      <li>If they win, you immediately end your turn.</li>
    </ul>
    <li>Whatever the fuck else</li>
    <ul>
      <li>
        Ask to do something and the DM will or will not allow it. Literally
        anything. Go crazy. Screw the rules lmao
      </li>
    </ul>
  </ul>
  <h3>Available Reactions</h3>
  <ul>
    <li>Dodge</li>
    <ul>
      <li>
        Allows an Acrobatics check to dodge Melee or Ranged attacks. When
        Dodging weapons with rate above 1 (guns, claws, etc), only roll once,
        and the enemy rolls once per Hit against that check.
      </li>
    </ul>
    <li>Block</li>
    <ul>
      <li>Allows a Blocking check with a blocking item (Sword/Shield).</li>
      <li>Add your Strength modifier when blocking Melee attacks.</li>
      <li>
        When blocking a Melee attack, when your Block roll is the same as the
        enemy's Hit roll, you Parry their attack.
      </li>
      <li>
        Parrying ends the enemy's current turn and reduces their Reactions to 0.
        If it was their last action, skip their next turn instead.
      </li>
    </ul>
    <li>Attack of Opportunity</li>
    <ul>
      <li>
        If an enemy attempts to move away from melee distance, you can attempt
        to attack.
      </li>
      <li>
        They cannot Dodge or Block, instead using their Passive Dodge against
        both Melee and Ranged attacks.
      </li>
    </ul>
  </ul>
