<p>
  Attributes go from 0 to 10, and give +1 to the relevant Dice roll per level,
  starting at -4. (10 gives +6)
</p>
<p>
  During character creation, you generally have 42 points to spread across your
  stats, but the DM may specify a higher or lower value. If not specifically
  stated to be different, assume 42.
</p>
<ul>
  <li>Vitality</li>
  <li>Agility</li>
  <li>Strength</li>
  <li>Dexterity</li>
  <li>Charisma</li>
  <li>Perception</li>
  <li>Intelligence</li>
</ul>
