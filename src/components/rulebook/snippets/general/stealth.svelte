<h2>Stealth and Noise</h2>
<p>Stealth is an important aspect of this game.</p>
<br />
<p>
  Attacks coming from hiding deal a Critical Hit and cannot be blocked or
  dodged. If it is the first round of turns in combat, and you begin hidden, you
  automatically get the first turn. If you then also get a higher Initiative
  roll than your opponent, you get to have a second turn before the enemy can
  retaliate, but this time out of Stealth. This stacks with multiple hidden
  members of a party, with you being able to take 4 turns (2 from Stealth, 2
  from higher Initiative) with 2 members before the enemy gets their own.
</p>
<br />
<p>
  While unseen, walking makes Loud Noise up to 2m away, and running up to 4m.
  Walking while Crouching does not make any noise.
</p>
<br />
<p>
  Loud Noises roll a 1d4 + DM-given modifiers dependant on what type of noise it
  is, with baselines below. The resulting roll is also the range in meters the
  noise can be heard from.
</p>
<br />
<p>
  Enemies within 4m can hear any Loud Noise no matter what. The enemy's Passive
  Perception is used to determine if they hear it. If they are actively
  searching for someone, instead use an Investigation roll.
</p>
