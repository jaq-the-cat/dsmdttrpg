<h2>Abilities</h2>
<h3>Wings</h3>
<p>Can be activated without spending an Action.</p>
<p>Permits flight, but may not be used as a weapon or shield.</p>
<p>When active, temporarily increases Agility by +1.</p>
<h3>Talon Carry</h3>
<p>
  Avian Drones can pick up Heavy objects such as other Drones, heavy weapons or
  containers using their Talons. This halves their Flying Speed.<br />
  The total weight they can carry is their Strength value multiplied by 10, up to
  100kg at 10 Strength.
</p>
<h3>Flying</h3>
<p>
  Avian Drones' wings are part of their arms, so they cannot have anything
  equipped in their hands while in flight. To use a weapon, they have to stop
  flying and start falling or be on the ground.
</p>
<h2>Drone Repair</h2>
<p>
  When there are no healing items available, Avian Drones can be repaired using
  parts, a tool such as screwdriver or wrench, and a bonding agent.
</p>
<p>The difficulty is the missing HP of the player to be healed.</p>
<p>Avian Drones can only be repaired with Avian Drone parts.</p>
<p>
  The healing amount is generally dependant on the specific parts used, but
  there are some static:
</p>
<ul>
  <li>Intact Drone Parts heal for 1d8 + Mechanics HP.</li>
  <li>Damaged Drone Parts heal for 1d4 + Mechanics HP.</li>
  <li>Replacing a damaged limb heals for 1/4 + Mechanics Max HP.</li>
</ul>
