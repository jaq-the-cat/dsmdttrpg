<h2>Abilities</h2>
<h3>Eat</h3>
<p>
  Can be used on a dead body (Human or Drone) to drain their entire Used Oil or
  Blood reserve at once. Eating someone during combat takes 2 turns to complete.
  Increases Heat by 2.
</p>
<h3>Regenerate (Absolute Solver > 1)</h3>
<p>
  Regenerate your wounds with an Absolute Solver roll. Difficulty is your
  missing HP up to 18.
</p>
<h3>Arm Weapons</h3>
<p>
  Hands/Fists, Claws, Sword, Chainsaw, Submachine Gun, Laser, Missile, Freakin'
  Ninja Star, EMP, Toy Items (Bubble Blower, Flag, etc.)
</p>
<p>Switching from Hands while holding something in that hand drops it.</p>
<h3>Wings</h3>
<p>Must be activated using an Action.</p>
<p>Permits flight, may also be used as a weapon or shield.</p>
<p>1d6 + Strength + 6 Strike damage</p>
<p>Block -2 difficulty</p>
<h3>Nanite Acid Stinger</h3>
<p>+1 Successful Attacks if hit.</p>
<p>Cannot be Blocked.</p>
<p>
  Melee, 3 damage per turn for 1d(Absolute Solver) turns or until licked by a
  Disassembly Drone, range 2m.
</p>
