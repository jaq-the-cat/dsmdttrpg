<h2>Absolute Solver</h2>
<p>Solver-enabled Drones have a special attribute called Absolute Solver.</p>
<p>
  If it reaches 10, the Drone has to roll a Willpower check at difficulty 10 +
  Missing HP. If failed, they fall under the influence of the Solver. While
  under its influence, they become an enemy. They can come back to normal if
  another person succeeds in a Persuasion check. If they have an emotional
  connection (partner, family member, etc) to the Drone under the influence of
  the Solver, gain Advantage.
</p>
<p>
  An Absolute Solver roll is different from regular Stats, with the roll bonus
  being Stat - 2, as opposed to Stat - 4, going up to +8 at 10.
</p>
<p>
  Absolute Solver goes up by +1 for every 10 Used Oil (1 whole Drone/Human) you
  consume.
</p>
<p>Having Absolute Solver above 8 turns the Drone's visor color to Yellow.</p>
