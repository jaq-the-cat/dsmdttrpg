<h2>Oil</h2>
<p>
  Non-Solver Drones have a Fresh and Used Oil reserve of 10. They turn 2 Fresh
  Oil into 2 Used Oil during a Short Rest and 5 Fresh Oil into 5 Used Oil during
  a Long Rest. They can replenish their Fresh Oil by drinking from a Fresh Oil
  reserve or by eating Batteries at a rate of 1 Fresh Oil per Battery.
</p>
<p>They can dump their current Used Oil reserve by using the Bathroom.</p>
<p>
  Whenever they take more than 4 damage, they roll a 1d4. They lose that amount
  in Fresh Oil down to 1. If they take any damage while at 1 Oil, go down to 0.
</p>
<p>
  At 0 Fresh Oil, take Max HP / 10 damage per turn until death or replenishment.
</p>
<p>At 10 Used Oil, take Max HP / 10 damage per turn until death or disposal.</p>
