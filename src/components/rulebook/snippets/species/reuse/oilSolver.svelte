<h2>Oil</h2>
<p>
  Solver-enabled Drones have a reserve of (10 - Absolute Solver level) Used Oil.
  They spend 2 Used Oil during a Short Rest and 5 Used Oil during a Long Rest,
  and have the option of spending 1 Used Oil to bring Heat and Absolute Solver
  down by 2.
</p>
<p>
  They can replenish their Used Oil by using Bite (with or without consent) on a
  Drone or a Human at a rate of 1 Blood to 1 Used Oil or by using Eat on a dead
  body.
</p>
<p>At 0 Used Oil, the Drone gains 1 Heat per turn.</p>
<p>
  Whenever they take more than 4 damage, they roll a 1d4. They lose that amount
  in Used Oil down to 1. If they take damage while at 1 Oil, go down to 0.
</p>
