<h2>Sanity</h2>
<p>
  Sanity is increased after a Long Rest by 5. If it reaches 0, they go Insane,
  allowing them to use Bite and Eat on other Drones. Eating a Human or Drone
  without being Insane triggers a Sanity check, with the difficulty being your
  missing HP. If you fail, become Insane.
</p>
<h2>Traumatic Events</h2>
<p>
  Decreases Sanity by 1. Can happen in a variety of ways, including watching
  another player die or another Human or Drone get eaten.
</p>
