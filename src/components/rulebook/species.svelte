  <details id="human">
    <summary>Human</summary>
    <h2>Stats</h2>
    <ul>
      <li>HP (2 + Vitality value x 2)</li>
      <li>Blood (10)</li>
      <li>Sanity (10)</li>
      <li>Resistant to Pierce and Energy damage</li>
    </ul>
    <h2>Speed</h2>
    <ul>
      <li>Walk Speed: 5</li>
      <li>Run Speed: 8</li>
      <li>Jump Height: 2</li>
      <li>Swim Speed: 2</li>
    </ul>
    <h2>Blood</h2>
    <p>
      Humans have a Blood reserve of 10. They spend 1 Blood during a Short Rest
      and 2 Blood during a Long Rest. Their source is fresh water and human
      food. A slice of Bread or cup of Water replenishes 2 Blood. Due to the
      variety of Human foods, the DM can choose how much Blood other kinds
      replenish, but these are the baselines.
    </p>
    <p>
      Blood can be used to Heal at a rate of 1 Blood to 2 HP. You cannot attack
      in the same turn you use to Heal using Blood.
    </p>
    <p>It can be replenished by eating or drinking.</p>
    <p>
      At 0 Blood, take Max HP / 10 damage per turn until death or replenishment.
    </p>
    <h2>Sanity</h2>
    <p>
      Sanity is increased after a Long Rest by 2. If it reaches 0, they go
      Insane, allowing them to use Bite and Eat on other Humans. Eating a Human
      without being Insane triggers a Sanity check, with the difficulty being
      your missing HP. If you fail, become Insane.
    </p>
    <h2>Traumatic Events</h2>
    <p>
      Decreases Sanity by 1. Can happen in a variety of ways, including watching
      another player die or another Human or Drone get eaten.
    </p>
    <h2>Dronification [NC]</h2>
    <p>
      A Human that gets a bit too intimate with JCJensonâ„¢ Nanites may be turned
      into a Drone. The type of Drone is decided by the infecting Drone's type
      or, if caused by an exterior factor, by rolling a 1d8, with 1-4 being a
      Worker Drone, 5-6 being a Solver Drone, and 7-8 being a Disassembly Drone.
    </p>
  </details>
  <details id="worker">
    <summary>Worker Drone</summary>
    <p>-1 Stealth while Visor is turned on. Toggle with an Action.</p>
    <ul>
      <li>HP (4 + Vitality value x 2)</li>
      <li>Fresh Oil (10)</li>
      <li>Used Oil (10)</li>
      <li>Sanity (10)</li>
      <li>Resistant to Strike damage</li>
    </ul>
    <h2>Speed</h2>
    <ul>
      <li>Walk Speed: 5</li>
      <li>Run Speed: 10</li>
      <li>Jump Height: 2</li>
    </ul>
    <h2>Modifiers</h2>
    <p>-1 Stealth while Visor is turned on. Toggle with an Action.</p>
    <p>
      +1 Expertises during Character Creation and increase limit from 2 to 3.
    </p>
    <h2>Oil</h2>
    <p>
      Worker Drones have a Fresh and Used Oil reserve of 10. They turn 2 Fresh
      Oil into 2 Used Oil during a Short Rest and 5 Fresh Oil into 5 Used Oil
      during a Long Rest. They can replenish their Fresh Oil by drinking from a
      Fresh Oil reserve or by eating Batteries at a rate of 1 Fresh Oil per
      Battery.
    </p>
    <p>They can dump their current Used Oil reserve by using the Bathroom.</p>
    <p>
      Whenever they take damage, they roll a 1d(Total Damage). They lose that
      amount in Fresh Oil.
    </p>
    <p>
      At 0 Fresh Oil, take Max HP / 10 damage per turn until death or
      replenishment.
    </p>
    <p>
      At 10 Used Oil, take Max HP / 10 damage per turn until death or disposal.
    </p>
    <h2>Drone Repair</h2>
    <p>
      When there are no healing items available, Worker Drones can be repaired
      using parts, a tool such as screwdriver or wrench, and a bonding agent.
    </p>
    <p>Worker Drones can be repaired with Worker or Disassembly Drone parts.</p>
    <p>The difficulty is the missing HP of the player to be healed.</p>
    <p>
      The healing amount is generally dependant on the specific parts used, but
      there are some static:
    </p>
    <ul>
      <li>Intact Drone Parts heal for 1d8 + Mechanics HP.</li>
      <li>Damaged Drone Parts heal for 1d4 + Mechanics HP.</li>
      <li>Replacing a damaged limb heals for 1/4 + Mechanics Max HP.</li>
    </ul>
    <h2>Sanity</h2>
    <p>
      Sanity is increased after a Long Rest by 2. If it reaches 0, they go
      Insane, allowing them to use Bite and Eat on other Drones. Eating a Drone
      without being Insane triggers a Sanity check, with the difficulty being
      your missing HP. If you fail, become Insane.
    </p>
    <h2>Traumatic Events</h2>
    <p>
      Decreases Sanity by 1. Can happen in a variety of ways, including watching
      another player die or another Human or Drone get eaten.
    </p>
    <h2>Disassembly Drone Part Scavenging [NC]</h2>
    <p>
      Worker Drones can choose to replace their own limbs with scavenged
      Disassembly Drone limbs.
    </p>
    <p>The roll difficulty is 10 + your missing HP.</p>
    <p>
      Succeeding in limb replacement triggers a Solver Infection roll of 1d(10 -
      Limbs Replaced).
    </p>
    <p>
      DD Arms grant them a hand and one singular weapon, the last one used by
      the Disassembly Drone before the limb was removed.
    </p>
    <p>Switching weapons when using a DD arm requires 1 Fresh or Used Oil.</p>
    <p>
      DD Legs, when they are both replaced, increase running speed to regular
      Disassembly Drone levels and jump height to 6 meters.
    </p>
    <h2>Solver Infection [NC]</h2>
    <p>
      If a Worker Drone uses Eat on a Solver-infected Drone body, or a
      Solver-enabled Drone uses Bite on a Worker Drone, the DM rolls a hidden
      1d10. A value of 1 turns the Worker into a Solver Drone.
    </p>
    <p>This should be kept secret from the player.</p>
    <p>
      Drones that have been infected in the previous or current game sessions
      have their Oil loss and Heat gain doubled by all sources and cannot infect
      Worker Drones using Bite.
    </p>
